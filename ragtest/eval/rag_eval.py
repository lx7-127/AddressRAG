#!/usr/bin/env python3
"""Evaluate saved RAG outputs with the current Ragas collections API.

----------------------

    user_input          The test question.
    retrieved_contexts  Context chunks returned by the RAG retriever, in rank order.
    reference           The human-checked reference answer.
    response            The answer generated by the RAG system.

Outputs
-------
    ragas_row_scores.csv      One row per QA pair and its four Ragas scores.
    ragas_summary.csv         Count, mean, standard deviation, and range.
    bootstrap_ci.csv          Percentile confidence intervals for mean scores.
    ragas_run_metadata.json   Reproducibility and runtime metadata.

The bootstrap uses QA pairs as the resampling unit. In each iteration, N rows
are sampled with replacement from the N evaluated rows. The same sampled row
indices are used for all metrics.


"""

