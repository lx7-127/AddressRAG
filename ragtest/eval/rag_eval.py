def write_dry_run(records: Sequence[EvaluationRecord], args: argparse.Namespace) -> None:
    pd, _ = import_tabular_dependencies()
    args.output_dir.mkdir(parents=True, exist_ok=True)
    preview = pd.DataFrame(
        {
            "sample_id": [record.sample_id for record in records],
            "user_input": [record.user_input for record in records],
            "retrieved_contexts": [
                json.dumps(record.retrieved_contexts, ensure_ascii=False) for record in records
            ],
            "reference": [record.reference for record in records],
            "response": [record.response for record in records],
        }
    )
    path = args.output_dir / "ragas_normalized_input_preview.csv"
    preview.to_csv(path, index=False, encoding="utf-8-sig")
    LOGGER.info("Dry run passed for %d rows; wrote %s", len(records), path)


def main(argv: Sequence[str] | None = None) -> int:
    args = parse_args(argv)
    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s | %(levelname)s | %(message)s",
    )
    try:
        records = load_records(args)
        LOGGER.info("Loaded %d valid QA pairs from %s", len(records), args.input)
        if args.dry_run:
            write_dry_run(records, args)
            return 0

        asyncio_module = import_asyncio()
        scores = asyncio_module.run(run_evaluation(records, args))
        row_scores = records_to_dataframe(records, scores)

        # Preserve row-level output even if aggregation later rejects missing scores.
        args.output_dir.mkdir(parents=True, exist_ok=True)
        row_scores.to_csv(
            args.output_dir / "ragas_row_scores.csv", index=False, encoding="utf-8-sig"
        )
        summary, bootstrap = summarize_and_bootstrap(row_scores, args)
        write_outputs(records, row_scores, summary, bootstrap, args)

        print("\nAggregate Ragas results")
        print(summary.to_string(index=False, float_format=lambda value: f"{value:.6f}"))
        if not bootstrap.empty:
            print("\nBootstrap confidence intervals")
            print(bootstrap.to_string(index=False, float_format=lambda value: f"{value:.6f}"))
        return 0
    except KeyboardInterrupt:
        LOGGER.error("Evaluation cancelled by user.")
        return 130
    except Exception as exc:
        LOGGER.error("Evaluation failed: %s", exc)
        if args.verbose:
            LOGGER.exception("Detailed traceback")
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
