@dataclass(frozen=True)
class EvaluationRecord:
    sample_id: str
    user_input: str
    retrieved_contexts: list[str]
    reference: str
    response: str


@dataclass(frozen=True)
class MetricSpec:
    name: str
    scorer: Any
    arguments: Callable[[EvaluationRecord], dict[str, Any]]


